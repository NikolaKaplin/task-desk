"use client"

import type React from "react"
import { FormProvider, useForm } from "react-hook-form"
import { zodResolver } from "@hookform/resolvers/zod"
import { registerUser } from "@/app/actions"
import { type TFormRegisterValues, RegisterSchema } from "./schema"
import { FormInput } from "./form-input"
import { Button } from "@/components/ui/button"
import { useToast } from "@/hooks/use-toast"
import { useRouter } from "next/navigation"
import { signIn } from "next-auth/react"

export const RegisterForm: React.FC = () => {
  const router = useRouter()
  const { toast } = useToast()
  const form = useForm<TFormRegisterValues>({
    resolver: zodResolver(RegisterSchema),
    defaultValues: {
      firstName: "",
      lastName: "",
      email: "",
      password: "",
    },
  })

  const onSubmit = async (data: TFormRegisterValues) => {
    try {
      const result = await registerUser(data)
      if (result.success) {
        toast({
          variant: 'default',
          title: "ü§ù",
          description: "–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ Altergemu",
        })
        await signIn("credentials", {
          email: data.email,
          password: data.password,
          redirect: false,
        })
        router.push("/login/confirm")
      } else {
        toast({
          variant: "destructive",
          title: "‚òù",
          description: "–í–∞—à–∞ –∑–∞—è–≤–∫–∞ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∞",
        })
      }
    } catch (err) {
      console.error(err)
      toast({
        variant: "destructive",
        title: "–û—à–∏–±–∫–∞",
        description: "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞—Ç—å—Å—è",
      })
    }
  }

  return (
    <FormProvider {...form}>
      <div className="space-y-4">
      <FormInput name="lastName" label="–§–∞–º–∏–ª–∏—è" required />
        <FormInput name="firstName" label="–ò–º—è" required />
        <FormInput name="email" label="E-Mail" required />
        <FormInput name="password" label="–ü–∞—Ä–æ–ª—å" type="password" required />
        <Button
          type="submit"
          disabled={form.formState.isSubmitting}
          className="w-full h-12 text-base bg-green-500 hover:bg-green-600 text-white"
          onClick={form.handleSubmit(onSubmit)}
        >
          {form.formState.isSubmitting ? "–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è..." : "–ó–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞—Ç—å—Å—è"}
        </Button>
      </div>
    </FormProvider>
  )
}

